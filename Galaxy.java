class Galaxy{
   static int galaxyId;
   static String spaces;
   static String oldest ;
   static String name;
 
	static void setGalaxyId(int id){
		galaxyId = id;
	}
    static int getGalaxyId(){
		return galaxyId;
	}

	static void setSpaces(String space){
		spaces = space;
	}
    static String getSpaces(){
		return spaces;
	}
 
    static void setOldest(String old){
		oldest = old;
	}
    static String getOldest(){
	    return oldest;
    }
	
	static void setName(String nam){
		name = nam;
	}
	static String getName(){
		return name;
	}
	}
	