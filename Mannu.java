class Mannu{

  static String texture;
  static String colour;
  static String itemWeight;
  static String brand;
  
  static void setTexture(String text){
     texture = text;
  }
  static String getTexture(){
     return texture;
  }
  
  static void setColour(String color){
	  colour = color;
  }
  static String getColour(){
	  return colour;
  }
  
  static void setItemWeight(String weight){
	  itemWeight = weight;
  }
  static String getItemWeight(){
	  return itemWeight;
  }
  
  static void setBrand(String band){
	  brand = band;
  }
  static String getBrand(){
	  return brand;
  }
 }
 